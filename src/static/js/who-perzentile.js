/**
 * Zeichnet Perzentilkurven auf ein Canvas.
 * Wählt automatisch den passenden Datensatz (0-24 oder 24-60) basierend auf dem Alter der Messpunkte.
 *
 * @param {string} canvasId - Canvas-Element ID
 * @param {string} datenKey - z.B. 'gewicht_m', 'groesse_w'
 * @param {Array} punkte - [{alter_monate, wert}]
 * @param {string} einheit - 'kg', 'cm'
 * @param {string} farbe - CSS-Farbe für die Messpunkte
 */
window.drawPerzentilChart = function(canvasId, datenKey, punkte, einheit, farbe) {
    const canvas = document.getElementById(canvasId);
    if (!canvas) return;
    const ctx = canvas.getContext('2d');
    const dpr = window.devicePixelRatio || 1;

    const rect = canvas.getBoundingClientRect();
    canvas.width = rect.width * dpr;
    canvas.height = rect.height * dpr;
    ctx.scale(dpr, dpr);
    const W = rect.width;
    const H = rect.height;

    // Automatische Datensatz-Wahl: Wenn ein Messpunkt > 24 Monate existiert -> 2-5 Jahre
    const maxAlter = punkte.length ? Math.max(...punkte.map(p => p.alter_monate)) : 0;
    const use25 = maxAlter > 24;
    const data = use25 ? (WHO_2_5[datenKey] || WHO[datenKey]) : WHO[datenKey];
    if (!data) return;

    const PAD = {top: 20, right: 20, bottom: 35, left: 45};
    const chartW = W - PAD.left - PAD.right;
    const chartH = H - PAD.top - PAD.bottom;

    const minMonat = data.monate[0];
    const maxMonat = data.monate[data.monate.length - 1];

    // Wertbereich berechnen
    const allVals = [...data.p3, ...(data.p97 || []), ...punkte.map(p => p.wert)].filter(v => v != null);
    const minVal = Math.floor(Math.min(...allVals) * 0.95);
    const maxVal = Math.ceil(Math.max(...allVals) * 1.03);

    function x(monat) { return PAD.left + ((monat - minMonat) / (maxMonat - minMonat)) * chartW; }
    function y(val) { return PAD.top + chartH - ((val - minVal) / (maxVal - minVal)) * chartH; }

    // Hintergrund
    ctx.fillStyle = '#fff';
    ctx.fillRect(0, 0, W, H);

    // Grid
    ctx.strokeStyle = '#f0e8ec';
    ctx.lineWidth = 0.5;
    const monatStep = maxMonat <= 24 ? 3 : 6;
    for (let m = minMonat; m <= maxMonat; m += monatStep) {
        ctx.beginPath(); ctx.moveTo(x(m), PAD.top); ctx.lineTo(x(m), PAD.top + chartH); ctx.stroke();
    }
    const step = einheit === 'kg' ? (maxVal > 20 ? 2 : 1) : (maxVal > 80 ? 10 : 5);
    for (let v = Math.ceil(minVal / step) * step; v <= maxVal; v += step) {
        ctx.beginPath(); ctx.moveTo(PAD.left, y(v)); ctx.lineTo(PAD.left + chartW, y(v)); ctx.stroke();
    }

    // Perzentilbänder zeichnen
    const bands = [
        {low: 'p3', high: 'p97', color: 'rgba(93,122,84,0.07)'},
        {low: 'p15', high: 'p85', color: 'rgba(93,122,84,0.09)'},
    ];
    for (const band of bands) {
        if (!data[band.low] || !data[band.high]) continue;
        ctx.fillStyle = band.color;
        ctx.beginPath();
        for (let i = 0; i < data.monate.length; i++) {
            const px = x(data.monate[i]);
            if (i === 0) ctx.moveTo(px, y(data[band.low][i]));
            else ctx.lineTo(px, y(data[band.low][i]));
        }
        for (let i = data.monate.length - 1; i >= 0; i--) {
            ctx.lineTo(x(data.monate[i]), y(data[band.high][i]));
        }
        ctx.closePath(); ctx.fill();
    }

    // Perzentillinien zeichnen
    const linien = [
        {key: 'p97', color: '#b8ccb2', label: 'P97', dash: [4,4]},
        {key: 'p85', color: '#8eab84', label: 'P85', dash: [2,3]},
        {key: 'p50', color: '#5d7a54', label: 'P50', dash: []},
        {key: 'p15', color: '#8eab84', label: 'P15', dash: [2,3]},
        {key: 'p3',  color: '#b8ccb2', label: 'P3',  dash: [4,4]},
    ];
    for (const l of linien) {
        if (!data[l.key]) continue;
        ctx.strokeStyle = l.color;
        ctx.lineWidth = l.key === 'p50' ? 2 : 1;
        ctx.setLineDash(l.dash);
        ctx.beginPath();
        for (let i = 0; i < data.monate.length; i++) {
            const px = x(data.monate[i]);
            const py = y(data[l.key][i]);
            if (i === 0) ctx.moveTo(px, py); else ctx.lineTo(px, py);
        }
        ctx.stroke();
        ctx.setLineDash([]);
        // Label
        ctx.fillStyle = l.color;
        ctx.font = '600 9px Nunito, sans-serif';
        const lastIdx = data.monate.length - 1;
        ctx.fillText(l.label, x(data.monate[lastIdx]) + 3, y(data[l.key][lastIdx]) + 3);
    }

    // Messpunkte zeichnen -- nur Punkte im sichtbaren Bereich
    const sichtbar = punkte.filter(p => p.wert != null && p.alter_monate >= minMonat && p.alter_monate <= maxMonat);

    ctx.setLineDash([]);
    if (sichtbar.length > 1) {
        ctx.strokeStyle = farbe || '#333';
        ctx.lineWidth = 2;
        ctx.beginPath();
        for (let i = 0; i < sichtbar.length; i++) {
            const px = x(sichtbar[i].alter_monate);
            const py = y(sichtbar[i].wert);
            if (i === 0) ctx.moveTo(px, py); else ctx.lineTo(px, py);
        }
        ctx.stroke();
    }

    for (const p of sichtbar) {
        const px = x(p.alter_monate);
        const py = y(p.wert);
        ctx.fillStyle = '#fff';
        ctx.beginPath(); ctx.arc(px, py, 5, 0, Math.PI * 2); ctx.fill();
        ctx.fillStyle = farbe || '#333';
        ctx.strokeStyle = farbe || '#333';
        ctx.lineWidth = 2.5;
        ctx.beginPath(); ctx.arc(px, py, 4, 0, Math.PI * 2); ctx.fill(); ctx.stroke();
    }

    // Achsen
    ctx.strokeStyle = '#ccc';
    ctx.lineWidth = 1;
    ctx.beginPath();
    ctx.moveTo(PAD.left, PAD.top); ctx.lineTo(PAD.left, PAD.top + chartH); ctx.lineTo(PAD.left + chartW, PAD.top + chartH);
    ctx.stroke();

    // Achsenbeschriftung
    ctx.fillStyle = '#999';
    ctx.font = '600 10px Nunito, sans-serif';
    ctx.textAlign = 'center';
    for (let m = minMonat; m <= maxMonat; m += monatStep) {
        ctx.fillText(m, x(m), PAD.top + chartH + 14);
    }
    ctx.fillText('Monate', PAD.left + chartW / 2, PAD.top + chartH + 30);

    ctx.textAlign = 'right';
    for (let v = Math.ceil(minVal / step) * step; v <= maxVal; v += step) {
        ctx.fillText(v, PAD.left - 5, y(v) + 3);
    }

    // Einheit
    ctx.save();
    ctx.translate(12, PAD.top + chartH / 2);
    ctx.rotate(-Math.PI / 2);
    ctx.textAlign = 'center';
    ctx.fillText(einheit, 0, 0);
    ctx.restore();
};
